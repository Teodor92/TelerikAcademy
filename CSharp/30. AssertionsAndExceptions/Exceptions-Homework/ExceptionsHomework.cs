        if (CheckPrime(firstNumber))
        {
            Console.WriteLine(firstNumber + " is prime.");
        }
        else
        {
            Console.WriteLine(firstNumber + " is not prime");
        }

        byte secondNum = 33;

        if (CheckPrime(secondNum))
        {
            Console.WriteLine(firstNumber + " is prime.");
